import UIKit

extension UIFont {
    static var appTitle: UIFont {
        return UIFont.systemFont(ofSize: 24)
    }
    static var appHeader: UIFont {
        return UIFont.systemFont(ofSize: 16)
    }
    static var appSubheader: UIFont {
        return UIFont.systemFont(ofSize: 15)
    }
    static var appBody: UIFont {
        return UIFont.systemFont(ofSize: 13)
    }
    static var appSubTitle: UIFont {
        return UIFont.systemFont(ofSize: 11)
    }
    static var appDescriptionText: UIFont {
        return UIFont.systemFont(ofSize: 8)
    }
}
